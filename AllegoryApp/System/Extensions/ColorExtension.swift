import Foundation
import UIKit

extension UIColor {

    public class var navyBlue: UIColor {
        return UIColor(red: 32/255, green: 42/255, blue: 68/255, alpha: 1.0)
    }
    public class var lilac: UIColor {
        return UIColor(red: 200/255, green: 162/255, blue: 200/255, alpha: 1.0)
    }

}
